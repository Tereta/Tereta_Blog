namespace Tereta\Blog\Controller\Blog;

use Magento\Framework\App\Action\Action;
use Magento\Framework\App\Action\Context;
use Magento\Framework\View\Result\PageFactory;
use Magento\Store\Model\StoreManagerInterface;
use Magento\Framework\App\Config\ScopeConfigInterface;
use Tereta\Blog\Model\BlogRepository;

class View extends Action
{
    /**
     * @var PageFactory
     */
    protected $pageFactory;

    /**
     * @var StoreManagerInterface
     */
    protected $storeManager;

    /**
     * @var ScopeConfigInterface
     */
    protected $scopeConfig;

    /**
     * @var BlogRepository
     */
    protected $blogRepository;

    /**
     * View constructor.
     * @param ScopeConfigInterface $scopeConfig
     * @param StoreManagerInterface $storeManager
     * @param Context $context
     * @param PageFactory $pageFactory
     * @param BlogRepository $blogRepository
     */
    public function __construct(
        ScopeConfigInterface $scopeConfig,
        StoreManagerInterface $storeManager,
        Context $context,
        PageFactory $pageFactory,
        BlogRepository $blogRepository
    ) {
        $this->blogRepository = $blogRepository;
        $this->scopeConfig = $scopeConfig;
        $this->storeManager = $storeManager;
        $this->pageFactory = $pageFactory;
        return parent::__construct($context);
    }

    /**
     * @return \Magento\Framework\App\ResponseInterface|\Magento\Framework\Controller\ResultInterface|\Magento\Framework\View\Result\Page
     * @throws \Magento\Framework\Exception\NoSuchEntityException
     */
    public function execute()
    {
        $blogModel = $this->blogRepository->getById($this->getRequest()->getParam('blog_id'));

        $page = $this->pageFactory->create();

        $page->getLayout()->getBlock('page.main.title')->setPageTitle($blogModel->getTitle());
        $page->getLayout()->getBlock('blog.description')->setData('blog_model', $blogModel);
        $page->getLayout()->getBlock('blog.listing')->setData('blog_model', $blogModel);

        if ($this->scopeConfig->getValue('general/store_information/name')){
            $page->getConfig()->getTitle()->set($this->scopeConfig->getValue('general/store_information/name'))->prepend($blogModel->getSeoTitle());
        }
        else {
            $page->getConfig()->getTitle()->set($blogModel->getSeoTitle());
        }
        $page->getConfig()->setKeywords($blogModel->getSeoKeywords());
        $page->getConfig()->setDescription($blogModel->getSeoDescription());

        if ($breadcrumbs = $page->getLayout()->getBlock('breadcrumbs')) {
            $breadcrumbs->addCrumb(
                'home',
                [
                    'label' => __('Home'),
                    'title' => __('Go to Home Page'),
                    'link'  => $this->storeManager->getStore()->getBaseUrl()
                ]
            );
            $breadcrumbs->addCrumb(
                'blog-' . $blogModel->getIdentifier(),
                [
                    'label' => $blogModel->getTitle()
                ]
            );
        }

        return $page;
    }
}