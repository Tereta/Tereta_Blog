namespace Tereta\Blog\Controller\Adminhtml\Blogs;

use Magento\Framework\View\Result\PageFactory;
use Magento\Framework\Controller\Result\RawFactory;
use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;

class PostGrid extends Action
{
    /**
     * @var PageFactory
     */
    protected $pageFactory;

    /**
     * @var RawFactory
     */
    protected $rawFactory;

    /**
     * PostGrid constructor.
     * @param RawFactory $rawFactory
     * @param PageFactory $pageFactory
     * @param Context $context
     */
    public function __construct(RawFactory $rawFactory, PageFactory $pageFactory, Context $context)
    {
        $this->rawFactory = $rawFactory;
        $this->pageFactory = $pageFactory;

        parent::__construct($context);
    }

    /**
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Tereta_Blog::blog');
    }

    /**
     * @return \Magento\Framework\App\ResponseInterface|\Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {
        $page = $this->pageFactory->create();
        $page->getLayout()->getBlock('tereta_blog.grid')->setData('blog_id', $this->getRequest()->getParam('entity_id'));
        $block = $page->getLayout()->getBlock('tereta_blog.grid.container')->toHtml();

        $result = $this->rawFactory->create();
        $result->setHeader('Content-Type', 'text/html');
        $result->setContents($block);
        return $result;
    }
}