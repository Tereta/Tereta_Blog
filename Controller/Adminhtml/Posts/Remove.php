namespace Tereta\Blog\Controller\Adminhtml\Posts;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Tereta\Blog\Model\PostFactory;

/**
 * Tereta\Blog\Controller\Adminhtml\Posts\Remove
 *
 * Class Remove
 * @package Tereta\Blog\Controller\Adminhtml\Posts
 */
class Remove extends Action
{
    /**
     * @var
     */
    protected $modelBlogFactory;

    /**
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Tereta_Blog::blog');
    }

    /**
     * Remove constructor.
     * @param Context $context
     * @param PostFactory $postFactory
     */
    public function __construct(Context $context, PostFactory $postFactory)
    {
        $this->modelPostFactory = $postFactory;

        parent::__construct($context);
    }

    /**
     * @return \Magento\Framework\App\ResponseInterface|\Magento\Framework\Controller\Result\Redirect|\Magento\Framework\Controller\ResultInterface
     * @throws \Exception
     */
    public function execute()
    {
        $resultRedirect = $this->resultRedirectFactory->create();

        $modelPost = $this->modelPostFactory->create();

        if ($this->getRequest()->getParam('entity_id')) {
            $modelPost->load($this->getRequest()->getParam('entity_id'));
        }
        else {
            return $resultRedirect->setPath('tereta_blog/posts/listing');
        }

        try {
            $modelPost->delete();

            $this->messageManager->addSuccessMessage(__('You deleted the blog.'));

            return $resultRedirect->setPath('tereta_blog/posts/listing');
        } catch (Exception $ex) {
            $this->messageManager->addErrorMessage($ex->getMessage());

            return $resultRedirect->setPath('tereta_blog/posts/listing');
        }
    }
}