namespace Tereta\Blog\Controller\Adminhtml\Posts;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Tereta\Blog\Model\PostFactory;

class Save extends Action
{
    /**
     * @var PostFactory
     */
    protected $modelPostFactory;

    /**
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Tereta_Blog::blog');
    }

    /**
     * Save constructor.
     * @param Context $context
     * @param PostFactory $postFactory
     */
    public function __construct(Context $context, PostFactory $postFactory)
    {
        $this->modelPostFactory = $postFactory;

        parent::__construct($context);
    }

    /**
     * @return \Magento\Framework\App\ResponseInterface|\Magento\Framework\Controller\Result\Redirect|\Magento\Framework\Controller\ResultInterface
     * @throws \Exception
     */
    public function execute()
    {
        $data = $this->getRequest()->getPostValue();

        $modelPost = $this->modelPostFactory->create();

        if ($this->getRequest()->getPost('entity_id')) {
            $modelPost->load($this->getRequest()->getPost('entity_id'));
        }
        else {
            unset($data['entity_id']);
        }

        try {
            if (isset($data['entity_id'])) {
                $modelPost->load($data['entity_id']);
            }

            $modelPost->setData($data);
            $modelPost->save();

            $this->messageManager->addSuccessMessage(__('You saved the blog.'));

            $resultRedirect = $this->resultRedirectFactory->create();
            return $resultRedirect->setPath('tereta_blog/posts/edit', ['entity_id' => $modelPost->getEntityId()]);
        } catch (Exception $ex) {
            $this->messageManager->addErrorMessage($ex->getMessage());

            return $resultRedirect->setPath('tereta_blog/posts/edit', ['entity_id' => $this->getRequest()->getPost('entity_id')]);
        }
    }
}