namespace Tereta\Blog\Model\Config\Source;

use Tereta\Blog\Model\ResourceModel\Blog\CollectionFactory as BlogCollectionFactory;

/**
 * Tereta\Blog\Model\Config\Source\Blogs
 *
 * Class Blogs
 * @package Tereta\Blog\Model\Config\Source
 */
class Blogs implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * @var BlogCollectionFactory
     */
    protected $blogCollectionFactory;

    /**
     * Blogs constructor.
     * @param BlogCollectionFactory $blogCollectionFactory
     */
    public function __construct(BlogCollectionFactory $blogCollectionFactory)
    {
        $this->blogCollectionFactory = $blogCollectionFactory;
    }

    /**
     * @return array
     */
    public function toOptionArray()
    {
        $dataList = [];

        $blogList = $this->blogCollectionFactory->create();

        foreach($blogList as $item) {
            array_push($dataList, [
                'value' => $item->getId(),
                'is_active' => true,
                'label' => $item->getIdentifier()
            ]);
        }

        return $dataList;
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return [];
    }
}