namespace Tereta\Blog\Model\Config\Source;

use Magento\Framework\Option\ArrayInterface;
use Magento\Store\Model\StoreManagerInterface;

/**
 * Tereta\Blog\Model\Config\Source\Stores
 *
 * Class Stores
 * @package Tereta\Blog\Model\Config\Source
 */
class Stores implements ArrayInterface
{
    /**
     * @var StoreManagerInterface
     */
    protected $_storeManager;

    /**
     * Stores constructor.
     * @param StoreManagerInterface $storeManager
     */
    public function __construct(StoreManagerInterface $storeManager) {
        $this->_storeManager = $storeManager;
    }

    /**
     * @return array
     */
    public function toOptionArray()
    {
        $return = [];

        array_push($return, ['value' => null, 'label' => __('Not Selected')]);
        foreach($this->_storeManager->getStores() as $storeModel){
            array_push($return, ['value' => $storeModel->getStoreId(), 'label' => $storeModel->getName()]);
        }

        return $return;
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return [];
    }
}