namespace Tereta\Blog\Model\Ui\Posts;

use Magento\Framework\View\Element\UiComponent\DataProvider\DataProvider as DataProviderExtend;
use Magento\Framework\Api\Search\ReportingInterface;
use Magento\Framework\Api\Search\SearchCriteriaBuilder;
use Magento\Framework\App\RequestInterface;
use Magento\Framework\Api\FilterBuilder;
use Tereta\Blog\Model\ResourceModel\Post\CollectionFactory as PostCollectionFactory;
use Tereta\Blog\Model\Blog as BlogModelFactory;
use Tereta\Blog\Model\Post as PostModel;

/**
 * Tereta\Blog\Model\Ui\Posts\DataProvider
 *
 * Class DataProvider
 * @package Tereta\Blog\Model\Ui\Form
 */
class DataProvider extends DataProviderExtend
{
    /**
     * @var array
     */
    protected $_loadedData = [];

    /**
     * @var \Tereta\Blog\Model\ResourceModel\Post\Collection
     */
    protected $_postCollection;

    /**
     * @var PostModel
     */
    protected $_postModel;

    /**
     * DataProvider constructor.
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param ReportingInterface $reporting
     * @param SearchCriteriaBuilder $searchCriteriaBuilder
     * @param RequestInterface $request
     * @param FilterBuilder $filterBuilder
     * @param PostModel $postModel
     * @param PostCollectionFactory $postCollectionFactory
     * @param array $meta
     * @param array $data
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        ReportingInterface $reporting,
        SearchCriteriaBuilder $searchCriteriaBuilder,
        RequestInterface $request,
        FilterBuilder $filterBuilder,
        PostModel $postModel,
        PostCollectionFactory $postCollectionFactory,
        array $meta = array(),
        array $data = array()
    ) {
        $this->_postCollection = $postCollectionFactory->create();
        $this->_postModel = $postModel;
        
        parent::__construct(
            $name,
            $primaryFieldName,
            $requestFieldName,
            $reporting,
            $searchCriteriaBuilder,
            $request,
            $filterBuilder,
            $meta,
            $data
        );
    }

    /**
     * @return array
     */
    public function getData() {
        $blogId = $this->request->getParam('blog_id');
        $postId = $this->request->getParam('entity_id');

        if ($blogId){
            $this->_loadedData[null] = ['blog_id' => $blogId];
        }

        if (!$postId) {
            return $this->_loadedData;
        }

        $items = $this->_postCollection;
        $items->addFieldToFilter('entity_id', $postId);

        if ($this->_loadedData || !count($items)) {
            return $this->_loadedData;
        }
        
        foreach ($items as $item) {
            $this->_loadedData[$item->getId()] = $item->getData();
        }

        return $this->_loadedData;
    }
}