namespace Tereta\Blog\Model\Ui\Posts\DataProvider;

use Magento\Framework\Api\FilterBuilder;
use Magento\Framework\Api\Search\ReportingInterface;
use Magento\Framework\Api\Search\SearchCriteriaBuilder;
use Magento\Framework\Api\Search\SearchResultInterface;
use Magento\Framework\App\RequestInterface;
use Magento\Framework\View\Element\UiComponent\DataProvider\DataProvider as DataProviderParent;
use Tereta\Blog\Model\ResourceModel\Blog\CollectionFactory as BlogCollectionFactory;

/**
 * Tereta\Blog\Model\Ui\Posts\DataProvider\Grid
 *
 * Class Grid
 * @package Tereta\Blog\Model\Ui\Posts\DataProvider
 */
class Grid extends DataProviderParent
{
    /**
     * @var BlogCollectionFactory
     */
    protected $blogCollectionFactory;

    /**
     * Grid constructor.
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param ReportingInterface $reporting
     * @param SearchCriteriaBuilder $searchCriteriaBuilder
     * @param RequestInterface $request
     * @param FilterBuilder $filterBuilder
     * @param array $meta
     * @param array $data
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        ReportingInterface $reporting,
        SearchCriteriaBuilder $searchCriteriaBuilder,
        RequestInterface $request,
        FilterBuilder $filterBuilder,
        BlogCollectionFactory $blogCollectionFactory,
        array $meta = [],
        array $data = []
    ) {
        $this->blogCollectionFactory = $blogCollectionFactory;

        parent::__construct($name, $primaryFieldName, $requestFieldName, $reporting, $searchCriteriaBuilder, $request, $filterBuilder, $meta, $data);
    }

    /**
     * @param SearchResultInterface $searchResult
     * @return array
     */
    protected function searchResultToOutput(SearchResultInterface $searchResult)
    {
        $result = parent::searchResultToOutput($searchResult);
        $loadBlogIds = [];
        foreach($result['items'] as $key=>$item){
            if (!in_array($item['blog_id'], $loadBlogIds)) {
                array_push($loadBlogIds, $item['blog_id']);
            }
        }

        $blogCollection = $this->blogCollectionFactory->create();
        if ($loadBlogIds){
            $blogCollection->addFieldToFilter('entity_id', ["in" => [$loadBlogIds]]);
        }

        $titleAssoc = [];
        foreach($blogCollection as $itemBlog){
            $titleAssoc[$itemBlog->getEntityId()] = $itemBlog->getIdentifier();
        }

        foreach($result['items'] as $key=>$item) {
            $result['items'][$key]['blog_id'] = $titleAssoc[$item['blog_id']];
        }

        return $result;
    }

    /**
     * @inheritdoc
     */
    public function addFilter(\Magento\Framework\Api\Filter $filter)
    {
        switch ($filter->getField()){
            case('blog_id'):
                $this->addFilterModifyBlogId($filter);
                break;
        }

        return parent::addFilter($filter);
    }

    /**
     * @param $filter
     */
    protected function addFilterModifyBlogId($filter)
    {
        $value = trim($filter->getValue(), '%');
        if (((integer) $value) === $value){
            $filter->setConditionType('eq');
            $filter->setValue($value);
            return;
        }

        $blogCollection = $this->blogCollectionFactory->create();
        $blogCollection->addFieldToFilter('identifier', ["like" => ['%' . $value . '%']]);

        $blogIds = [];
        foreach($blogCollection as $item){
            array_push($blogIds, $item->getEntityId());
        }

        if (!$blogIds){
            $filter->setConditionType('eq');
            $filter->setValue(null);
            return;
        }

        $filter->setConditionType('in');
        $filter->setValue($blogIds);
    }
}