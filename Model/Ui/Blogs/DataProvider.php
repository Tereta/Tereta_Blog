namespace Tereta\Blog\Model\Ui\Blogs;

use Magento\Framework\View\Element\UiComponent\DataProvider\DataProvider as DataProviderExtend;
use Magento\Framework\Api\Search\ReportingInterface;
use Magento\Framework\Api\Search\SearchCriteriaBuilder;
use Magento\Framework\App\RequestInterface;
use Magento\Framework\Api\FilterBuilder;
use Tereta\Blog\Model\BlogRepository;

/**
 * Tereta\Blog\Model\Ui\Blogs\DataProvider
 *
 * Class DataProvider
 * @package Tereta\Blog\Model\Ui\Form
 */
class DataProvider extends DataProviderExtend
{
    /**
     * @var array
     */
    protected $_loadedData = [];

    /**
     * @var BlogRepository
     */
    protected $blogRepository;

    /**
     * DataProvider constructor.
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param ReportingInterface $reporting
     * @param SearchCriteriaBuilder $searchCriteriaBuilder
     * @param RequestInterface $request
     * @param FilterBuilder $filterBuilder
     * @param BlogRepository $blogRepository
     * @param array $meta
     * @param array $data
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        ReportingInterface $reporting,
        SearchCriteriaBuilder $searchCriteriaBuilder,
        RequestInterface $request,
        FilterBuilder $filterBuilder,
        BlogRepository $blogRepository,
        array $meta = array(),
        array $data = array()
    ) {
        $this->blogRepository = $blogRepository;

        parent::__construct(
            $name,
            $primaryFieldName,
            $requestFieldName,
            $reporting,
            $searchCriteriaBuilder,
            $request,
            $filterBuilder,
            $meta,
            $data
        );
    }

    /**
     * @return array
     */
    public function getData() {
        $blogId = $this->request->getParam('entity_id');

        if (!$blogId) {
            return $this->_loadedData;
        }

        $blogModel = $this->blogRepository->getById($blogId);

        if ($this->_loadedData || !$blogModel) {
            return $this->_loadedData;
        }

        $blogModel->setData('path_url', $blogModel->getUrl());
        $this->_loadedData[$blogModel->getEntityId()] = $blogModel->getData();

        return $this->_loadedData;
    }
}