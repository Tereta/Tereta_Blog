namespace Tereta\Blog\Model;

use Tereta\Blog\Model\ResourceModel\Blog as ResourceModelBlog;
use Magento\Framework\Model\Context;
use Magento\Framework\Registry;
use Magento\Framework\Model\ResourceModel\AbstractResource;
use Magento\Framework\Data\Collection\AbstractDb;
use Magento\Store\Model\StoreManagerInterface;
use Magento\Framework\Exception\LocalizedException;
use Tereta\Blog\Model\Generic as GenericModel;
use Tereta\Blog\Model\ResourceModel\Post\CollectionFactory as PostCollectionFactory;

/**
 * Tereta\Blog\Model\Blog
 *
 * Class Blog
 * @package Tereta\Blog\Model
 */
class Blog extends GenericModel{
    /**
     * @var RewriteUrl
     */
    protected $rewriteUrlModel;

    /**
     * @var StoreManagerInterface
     */
    protected $storeManager;

    /**
     * @var RewriteUrl
     */
    protected $rewriteUrl;

    /**
     * @var PostCollectionFactory
     */
    protected $postCollectionFactory;

    /**
     * Blog constructor.
     * @param PostCollectionFactory $postCollectionFactory
     * @param RewriteUrl $rewriteUrl
     * @param StoreManagerInterface $storeManager
     * @param RewriteUrl $rewriteUrlModel
     * @param Context $context
     * @param Registry $registry
     * @param AbstractResource|null $resource
     * @param AbstractDb|null $resourceCollection
     * @param array $data
     */
    public function __construct(
        PostCollectionFactory $postCollectionFactory,
        RewriteUrl $rewriteUrl,
        StoreManagerInterface $storeManager,
        RewriteUrl $rewriteUrlModel,
        Context $context,
        Registry $registry,
        AbstractResource $resource = null,
        AbstractDb $resourceCollection = null,
        array $data = []
    ) {
        $this->postCollectionFactory = $postCollectionFactory;
        $this->rewriteUrl = $rewriteUrl;
        $this->storeManager = $storeManager;
        $this->rewriteUrlModel = $rewriteUrlModel;

        parent::__construct($context, $registry, $resource, $resourceCollection, $data);
    }

    /**
     *
     */
    protected function _construct()
    {
        $this->_init(ResourceModelBlog::class);
    }

    /**
     * @return string|void
     * @throws \Magento\Framework\Exception\NoSuchEntityException
     */
    public function getUrl()
    {
        if (!$this->getEntityId()){
            return;
        }

        return $this->rewriteUrl->getUrlByTarget('tereta_blog/blog/view/blog_id/' . $this->getEntityId(), $this->getStoreId());
    }

    /**
     * @return array|ResourceModel\Post\Collection
     */
    public function getPosts()
    {
        if (!$this->getEntityId()){
            return [];
        }

        $postCollection = $this->postCollectionFactory->create();
        $postCollection->addFieldToFilter('blog_id', $this->getEntityId());
        $postCollection->addFieldToFilter('is_active', true);
        return $postCollection;
    }

    /**
     * @return Generic
     * @throws LocalizedException
     */
    public function beforeSave()
    {
        if (!$this->getSeoUrlKey()) {
            $this->setSeoUrlKey($this->rewriteUrlModel->generateUrlKey($this->getTitle(), $this->getStoreId(), null, true));
        }

        if (!$this->getSeoTitle()) {
            $this->setSeoTitle($this->getTitle());
        }

        $rewriteModel = $this->rewriteUrl->getByUrl($this->getSeoUrlKey(), $this->getStoreId());

        if ($rewriteModel && ($rewriteModel->getEntityId() != $this->getEntityId() || $rewriteModel->getEntityType() != RewriteUrl::ENTITY_TYPE_BLOG)) {
            throw new LocalizedException(__('SEO URL already in use, please change it'));
        }

        return parent::beforeSave();
    }

    /**
     * @return Generic
     * @throws \Exception
     */
    public function afterSave()
    {
        $this->rewriteUrl->create(RewriteUrl::ENTITY_TYPE_BLOG, $this->getId(), $this->getSeoUrlKey(), $this->getStoreId());
        return parent::afterSave();
    }

    /**
     * @return Generic
     */
    public function afterDelete()
    {
        $this->rewriteUrl->clear(RewriteUrl::ENTITY_TYPE_BLOG, $this->getId());
        return parent::afterDelete();
    }
}