namespace Tereta\Blog\Model;

use Tereta\Blog\Model\Api\RepositoryInterface;

/**
 * Tereta\Blog\Model\PostRepository
 *
 * Class PostRepository
 * @package Tereta\Blog\Model
 */
class PostRepository implements RepositoryInterface
{
    /**
     * @var \Tereta\Blog\Model\PostFactory
     */
    protected $postFactory;

    /**
     * @var array
     */
    protected $postsById = [];

    /**
     * PostRepository constructor.
     * @param \Tereta\Blog\Model\PostFactory $postFactory
     */
    public function __construct(PostFactory $postFactory)
    {
        $this->postFactory = $postFactory;
    }

    /**
     * @param $blogId
     * @return mixed|void
     */
    public function getById($blogId)
    {
        if (isset($this->postsById[$blogId])) {
            return $this->postsById[$blogId];
        }

        $blogModel = $this->postFactory->create();
        $blogModel->load($blogId);
        if (!$blogModel->getEntityId()){
            $this->postsById[$blogId] = null;
            return;
        }

        $this->postsById[$blogId] = $blogModel;
        return $this->postsById[$blogId];
    }
}