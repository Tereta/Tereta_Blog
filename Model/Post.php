namespace Tereta\Blog\Model;

use Magento\Framework\Exception\LocalizedException;
use Tereta\Blog\Model\ResourceModel\Post as ResourceModelPost;
use Magento\Framework\Registry;
use Magento\Framework\Model\ResourceModel\AbstractResource;
use Magento\Framework\Data\Collection\AbstractDb;
use Magento\Framework\Model\Context;
use Tereta\Blog\Model\Generic as GenericModel;

/**
 * Tereta\Blog\Model\Post
 *
 * Class Blog
 * @package Tereta\Blog\Model
 */
class Post extends GenericModel{
    /**
     * @var RewriteUrl
     */
    protected $rewriteUrlModel;

    /**
     * @var BlogRepository
     */
    protected $blogRepository;

    /**
     * @var array
     */
    protected $blogModel = [];

    /**
     * Post constructor.
     * @param BlogRepository $blogRepository
     * @param RewriteUrl $rewriteUrlModel
     * @param Context $context
     * @param Registry $registry
     * @param AbstractResource|null $resource
     * @param AbstractDb|null $resourceCollection
     * @param array $data
     */
    public function __construct(
        BlogRepository $blogRepository,
        RewriteUrl $rewriteUrlModel,
        Context $context,
        Registry $registry,
        AbstractResource $resource = null,
        AbstractDb $resourceCollection = null,
        array $data = []
    ) {
        $this->blogRepository = $blogRepository;
        $this->rewriteUrlModel = $rewriteUrlModel;
        parent::__construct($context, $registry, $resource, $resourceCollection, $data);
    }

    /**
     *
     */
    protected function _construct()
    {
        $this->_init(ResourceModelPost::class);
    }

    /**
     * @return string
     * @throws \Magento\Framework\Exception\NoSuchEntityException
     */
    public function getUrl()
    {
        return $this->rewriteUrlModel->getUrlByTarget('tereta_blog/post/view/post_id/' . $this->getEntityId(), $this->getBlog()->getStoreId());
    }

    /**
     * @return mixed
     */
    public function getStoreId()
    {
        $blogModel = $this->getBlogModel();
        return $blogModel->getStoreId();
    }

    /**
     * @param null $entityId
     * @return mixed
     */
    public function getBlog()
    {
        if ($blogModel = $this->getData('blog'))
        {
            return $blogModel;
        }

        $entityId = $this->getBlogId();
        $blogModel = $this->blogRepository->getById($entityId);
        $this->setData('blog', $blogModel);

        return $blogModel;
    }

    /**
     * @return Generic
     * @throws LocalizedException
     */
    public function beforeSave()
    {
        $blogModel = null;
        if ($this->getBlogId() && $blogModel = $this->getBlog()) {
            $this->setStoreId($blogModel->getStoreId());
        }

        if (!$this->getSeoUrlKey()) {
            $blogSeoUrlKey = null;
            if ($blogModel) {
                $blogSeoUrlKey = $blogModel->getSeoUrlKey();
            }

            $this->setSeoUrlKey($this->rewriteUrlModel->generateUrlKey($this->getTitle(), $this->getStoreId(), $blogSeoUrlKey, true));
        }

        if (!$this->getSeoTitle()) {
            $this->setSeoTitle($this->getTitle());
        }

        $rewriteModel = $this->rewriteUrlModel->getByUrl($this->getSeoUrlKey(), $this->getStoreId());

        if ($rewriteModel && ($rewriteModel->getEntityId() != $this->getEntityId() || $rewriteModel->getEntityType() != RewriteUrl::ENTITY_TYPE_POST)) {
            throw new LocalizedException(__('SEO URL already in use, please change it'));
        }

        return parent::beforeSave();
    }

    /**
     * @return Generic
     * @throws \Exception
     */
    public function afterSave()
    {
        $this->rewriteUrlModel->create(RewriteUrl::ENTITY_TYPE_POST, $this->getId(), $this->getSeoUrlKey(), $this->getStoreId());
        return parent::afterSave();
    }

    /**
     * @return Generic
     */
    public function afterDelete()
    {
        $this->rewriteUrlModel->clear(RewriteUrl::ENTITY_TYPE_POST, $this->getId());
        return parent::afterDelete();
    }
}