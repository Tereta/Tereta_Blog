namespace Tereta\Blog\Model;

use Tereta\Blog\Model\Api\RepositoryInterface;

/**
 * Tereta\Blog\Model\BlogRepository
 *
 * Class BlogRepository
 * @package Tereta\Blog\Model
 */
class BlogRepository implements RepositoryInterface
{
    /**
     * @var \Tereta\Blog\Model\BlogFactory
     */
    protected $blogFactory;

    /**
     * @var array
     */
    protected $blogsById = [];

    /**
     * BlogRepository constructor.
     * @param \Tereta\Blog\Model\BlogFactory $blogFactory
     */
    public function __construct(BlogFactory $blogFactory)
    {
        $this->blogFactory = $blogFactory;
    }

    /**
     * @param $blogId
     * @return mixed|void
     */
    public function getById($blogId)
    {
        if (isset($this->blogsById[$blogId])) {
            return $this->blogsById[$blogId];
        }

        $blogModel = $this->blogFactory->create();
        $blogModel->load($blogId);
        if (!$blogModel->getEntityId()){
            $this->blogsById[$blogId] = null;
            return;
        }

        $this->blogsById[$blogId] = $blogModel;
        return $this->blogsById[$blogId];
    }
}