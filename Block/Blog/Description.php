namespace Tereta\Blog\Block\Blog;

/**
 * Tereta\Blog\Block\Blog\Description
 *
 * Class Description
 * @package Tereta\Blog\Block\Blog
 */
class Description extends General
{
    /**
     * @var string
     */
    protected $_template = 'Tereta_Blog::blog/description.phtml';

    /**
     * @return string
     */
    protected function _toHtml()
    {
        $blogModel = $this->getBlog();
        $this->assign('description', $blogModel->getDescription());

        return parent::_toHtml();
    }
}