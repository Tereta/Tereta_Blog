namespace Tereta\Blog\Block\Blog;

use Magento\Framework\View\Element\Template;
use Magento\Framework\View\Element\Template\Context;
use Tereta\Blog\Model\BlogRepository;

/**
 * Tereta\Blog\Block\Blog\General
 *
 * Class General
 * @package Tereta\Blog\Block\Blog
 */
class General extends Template
{
    /**
     * @var BlogRepository
     */
    protected $blogRepository;

    /**
     * General constructor.
     * @param BlogFactory $blogFactory
     * @param Context $context
     * @param array $data
     */
    public function __construct(BlogRepository $blogRepository, Context $context, array $data = [])
    {
        $this->blogRepository = $blogRepository;

        parent::__construct($context, $data);
    }

    /**
     * @return mixed
     */
    protected function getBlog()
    {
        if ($this->getData('blog_model')){
            return $this->getData('blog_model');
        }

        if ($blogId = $this->getRequest()->getParam('blog_id')){
            $blogModel = $this->blogRepository->getById($blogId);
        }

        $this->setData('blog_model', $blogModel);
        return $this->getData('blog_model');
    }
}