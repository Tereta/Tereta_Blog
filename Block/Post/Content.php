namespace Tereta\Blog\Block\Post;

use Magento\Framework\View\Element\Template;
use Magento\Framework\View\Element\Template\Context;
use Tereta\Blog\Model\PostRepository;

/**
 * Tereta\Blog\Block\Post\Content
 *
 * Class Content
 * @package Tereta\Blog\Block\Post
 */
class Content extends Template
{
    protected $_template = 'Tereta_Blog::post/content.phtml';

    protected $postRepository;

    public function __construct(PostRepository $postRepository, Context $context, array $data = [])
    {
        $this->postRepository = $postRepository;
        parent::__construct($context, $data);
    }

    /**
     * @return mixed
     */
    protected function getPost()
    {
        if ($this->getData('post_model')){
            return $this->getData('post_model');
        }

        if ($postId = $this->getRequest()->getParam('post_id')){
            $postModel = $this->postRepository->getById($postId);
        }

        $this->setData('post_model', $postModel);
        return $this->getData('post_model');
    }

    public function _toHtml()
    {
        $postModel = $this->getPost();
        $this->assign('content', $postModel->getContent());

        return parent::_toHtml();
    }
}