namespace Tereta\Blog\Block\Adminhtml\Block\Edit;

use Magento\Backend\Block\Widget\Context;
use Magento\Framework\View\Element\UiComponent\Control\ButtonProviderInterface;
use Tereta\Blog\Model\PostFactory;

/**
 * Tereta\Blog\Block\Adminhtml\Block\Edit\BackBlogButton
 *
 * Class BackBlogButton
 * @package Tereta\Blog\Block\Adminhtml\Block\Edit
 */
class BackBlogButton extends GenericButton implements ButtonProviderInterface
{
    protected $postModelFactory;

    public function __construct(Context $context, PostFactory $postModelFactory)
    {
        $this->postModelFactory = $postModelFactory;
        parent::__construct($context);
    }

    /**
     * @return array
     */
    public function getButtonData()
    {
        if (!$this->getEntityId()){
            return;
        }

        return [
            'label' => __('Back to Blog'),
            'on_click' => sprintf("location.href = '%s';", $this->getBackUrl()),
            'class' => 'back',
            'sort_order' => 10
        ];
    }

    protected $blogId;

    public function getEntityId()
    {
        if (!is_null($this->blogId)){
            return $this->blogId;
        }

        if ($blogId = $this->context->getRequest()->getParam('blog_id')){
            $this->blogId = $blogId;
            return $this->blogId;
        }

        if ($postId = parent::getEntityId()){
            $postModel = $this->postModelFactory->create();
            $postModel->load($postId);
            $this->blogId = $postModel->getBlogId();
            return $this->blogId;
        }

        $this->blogId = false;
        return $this->blogId;
    }

    /**
     * Get URL for back (reset) button
     *
     * @return string
     */
    public function getBackUrl()
    {
        return $this->getUrl('tereta_blog/blogs/edit', ['entity_id' => $this->getEntityId()]);
    }
}
