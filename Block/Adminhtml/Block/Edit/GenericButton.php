namespace Tereta\Blog\Block\Adminhtml\Block\Edit;

use Magento\Backend\Block\Widget\Context;
use Magento\Framework\Exception\NoSuchEntityException;
use Tereta\Blog\Model\Generic as GenericModel;

/**
 * Tereta\Blog\Block\Adminhtml\Block\Edit\GenericButton
 *
 * Class GenericButton
 * @package Tereta\Blog\Block\Adminhtml\Block\Edit
 */
class GenericButton
{
    /**
     * @var Context
     */
    protected $context;

    /**
     * @var \Tereta\Blog\Model\Blog
     */
    protected $genericModel;

    /**
     * GenericButton constructor.
     *
     * @param Context $context
     * @param GenericModel $genericmodel
     */
    public function __construct(
        Context $context,
        GenericModel $genericModel = null
    ) {
        $this->context = $context;
        $this->genericModel = $genericModel;
    }

    public function getEntityId()
    {
        return $this->context->getRequest()->getParam('entity_id');
    }

    /**
     * Generate url by route and parameters
     *
     * @param   string $route
     * @param   array $params
     * @return  string
     */
    public function getUrl($route = '', $params = [])
    {
        return $this->context->getUrlBuilder()->getUrl($route, $params);
    }
}
