namespace Tereta\Blog\Block\Adminhtml\Block\Edit;

use Magento\Framework\View\Element\UiComponent\Control\ButtonProviderInterface;

/**
 * Tereta\Blog\Block\Adminhtml\Block\Edit\CreatePostButton
 *
 * Class CreatePostButton
 * @package Tereta\Blog\Block\Adminhtml\Block\Edit
 */
class CreatePostButton extends GenericButton implements ButtonProviderInterface
{
    /**
     * @return array
     */
    public function getButtonData()
    {
        if (!$this->getEntityId()){
            return [];
        }

        return [
            'label' => __('Create Post'),
            'on_click' => sprintf("location.href = '%s';", $this->getPostUrl()),
            'class' => 'primary',
            'sort_order' => 20
        ];
    }

    /**
     * Get URL for back (reset) button
     *
     * @return string
     */
    public function getPostUrl()
    {
        return $this->getUrl('*/posts/edit/blog_id/', ['blog_id' => $this->getEntityId()]);
    }
}
