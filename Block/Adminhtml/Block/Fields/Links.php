namespace Tereta\Blog\Block\Adminhtml\Block\Fields;

use Magento\Framework\View\Element\Template;
use Magento\Framework\View\Element\Template\Context;
use Tereta\Blog\Model\BlogRepository;
use Tereta\Blog\Model\Api\RepositoryInterface;

/**
 * Tereta\Blog\Block\Adminhtml\Block\Fields\Links
 *
 * Class Links
 * @package Tereta\Blog\Block\Adminhtml\Block\Fields
 */
class Links extends Template
{
    /**
     * @var string
     */
    protected $_template = "Tereta_Blog::fields/links.phtml";

    /**
     * @var BlogRepository
     */
    protected $repository;

    /**
     * Links constructor.
     * @param RepositoryInterface $modelRepository
     * @param Context $context
     * @param array $data
     */
    public function __construct(Context $context, RepositoryInterface $modelRepository = null, array $data = [])
    {
        $this->repository = $modelRepository;

        parent::__construct($context, $data);
    }

    /**
     * @return string
     */
    protected function _toHtml()
    {
        $entityId = $this->getRequest()->getParam('entity_id');
        $entityModel = $this->repository->getById($entityId);

        if (!$entityModel){
            return;
        }

        $this->assign('label', $this->getLabel());
        $this->assign('link', $entityModel->getUrl());

        return parent::_toHtml();
    }
}