namespace Tereta\Blog\Block\Adminhtml\Block\Widget\Post;

use Magento\Backend\Block\Widget\Grid as GridParent;

/**
 * Tereta\Blog\Block\Adminhtml\Block\Widget\Post\Grid
 *
 * Class Grid
 * @package Tereta\Blog\Block\Adminhtml\Block\Widget\Post
 */
class Grid extends GridParent
{
    /**
     * @return \Magento\Framework\Data\Collection
     * @throws \Magento\Framework\Exception\LocalizedException
     */
    public function getCollection()
    {
        $collection = parent::getCollection();
        if (!($blogId = $this->getData('blog_id'))){
            $blogId = $this->getParam('entity_id');
        }

        $collection->addFieldToFilter('blog_id', $blogId);
        return $collection;
    }
}